import TheFooter from '../../Components/TheFooter.vue';
import {
  animInit,
  container,
  header,
  isInView,
  sectionSlider,
  animateElement
} from '../../Helpers/main';
import { onMounted } from 'vue';

onMounted(() => {
  document.title = 'Madura Indah Wisata Travel & Tour';
  animInit();
  const anim = document.querySelectorAll('[data-anim]');
  anim.forEach((el: any) => isInView(el));
  const animChilds: any = document.querySelectorAll('[data-anim-child]');
  animChilds.forEach((el: any) => animateElement(el));
  header();
  new LazyLoad({
    elements_selector: '.js-lazy'
  });
  container();
  sectionSlider();
  testimonialsSlider();
});

function testimonialsSlider() {
  const slider = new Swiper('.js-testimonials-slider', {
    speed: 700,
    loop: true,
    lazy: {
      loadPrevNext: true,
    },
  });

  const paginationItems = document.querySelectorAll('.js-testimonials-slider .js-testimonials-pagination > * > *');

  paginationItems.forEach((el, i) => {
    el.addEventListener('click', () => {
      document.querySelector('.js-testimonials-slider .js-testimonials-pagination .is-active')?.classList.remove('is-active');
      el.classList.add('is-active');
      slider.slideTo(i + 1);
    });
  });

  slider.on('slideChangeTransitionEnd', () => {
    document.querySelector('.js-testimonials-slider .js-testimonials-pagination .is-active')?.classList.remove('is-active');
    paginationItems[slider.realIndex].classList.add('is-active');
  });
}

</script>

